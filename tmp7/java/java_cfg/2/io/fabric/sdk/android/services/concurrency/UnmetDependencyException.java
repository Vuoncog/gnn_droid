package io.fabric.sdk.android.services.concurrency;
public class UnmetDependencyException extends java.lang.RuntimeException {

    public UnmetDependencyException()
    {
        return;
    }

    public UnmetDependencyException(String p1)
    {
        super(p1);
        return;
    }

    public UnmetDependencyException(String p1, Throwable p2)
    {
        super(p1, p2);
        return;
    }

    public UnmetDependencyException(Throwable p1)
    {
        super(p1);
        return;
    }
}
