package org.apache.commons.lang;
public class SerializationException extends org.apache.commons.lang.exception.NestableRuntimeException {
    private static final long serialVersionUID = 4029025366392702726;

    public SerializationException()
    {
        return;
    }

    public SerializationException(String p1)
    {
        super(p1);
        return;
    }

    public SerializationException(String p1, Throwable p2)
    {
        super(p1, p2);
        return;
    }

    public SerializationException(Throwable p1)
    {
        super(p1);
        return;
    }
}
